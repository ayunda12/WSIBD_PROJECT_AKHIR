     public void datatable() {
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("Kode Paket");
        tbl.addColumn("Nama Paket");
        tbl.addColumn("Satuan");
        tbl.addColumn("Harga");
        tabelpaket.setModel(tbl);

        try {
            Statement statement = (Statement) Koneksi_table.GetConnection().createStatement();
            ResultSet res = statement.executeQuery("select * from tb_paketlaundry");
            while (res.next()) {
                tbl.addRow(new Object[]{
                    res.getString("kode_paket"),
                    res.getString("nama_paket"),
                    res.getString("satuan"),
                    res.getString("harga")

                });
                tabelpaket.setModel(tbl);
            }

        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, "salah");
        }
    }

    private void cariData(String key) {
        try {

            DefaultTableModel tbl = new DefaultTableModel();
            tbl.addColumn("Kode Paket");
            tbl.addColumn("Nama Paket");
            tbl.addColumn("Satuan");
            tbl.addColumn("Harga");
            
            tabelpaket.setModel(tbl);

            Statement statement = (Statement) Koneksi_table.GetConnection().createStatement();
            tbl.getDataVector().removeAllElements();

            RsProduk = statement.executeQuery("SELECT * from tb_paketlaundry WHERE kode_paket "
                    + "LIKE '%" + key + "%' OR nama_paket LIKE '%" + key + "%' ");
            while (RsProduk.next()) {

                Object[] data = {
                    RsProduk.getString("kode_paket"),
                    RsProduk.getString("nama_paket"),
                     RsProduk.getString("satuan"),
                    RsProduk.getString("harga")
                };
                tbl.addRow(data);
            }
        } catch (Exception ex) {
            System.err.println(ex.getMessage());
        }
    }

